#include "libft.h"

char		*ft_strncpy(char *dst, const char *src, size_t n)
{
	char	*d;

	d = dst;
	while (n > 0 && *src)
	{
		*d++ = *src++;
		--n;
	}
	while (n > 0)
	{
		*d++ = '\0';
		--n;
	}
	return (dst);
}
